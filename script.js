let currentLanguage = "en";

function setLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = translations[language][key] || translations.en[key] || "";
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    element.placeholder = translations[language][key] || translations.en[key] || "";
  });

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === language);
  });
}

document.querySelectorAll(".lang-btn").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

document.getElementById("bookingForm").addEventListener("submit", (event) => {
  event.preventDefault();

  const form = new FormData(event.currentTarget);
  const text = translations[currentLanguage];
  const body = [
    text.emailIntro,
    "",
    `${text.labelName}: ${form.get("name")}`,
    `${text.labelPhone}: ${form.get("contact")}`,
    `${text.labelGuests}: ${form.get("guests")}`,
    `${text.labelDate}: ${form.get("date")}`,
    `${text.labelMessage}: ${form.get("message") || "-"}`
  ].join("\n");

  const mailto = `mailto:${bookingEmail}?subject=${encodeURIComponent(text.emailSubject)}&body=${encodeURIComponent(body)}`;
  window.location.href = mailto;
});

setLanguage(currentLanguage);
